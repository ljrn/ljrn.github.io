//Writing effect
var speed = 50;
var i = 0;
var txt = 'Software Engineer / Data Analyst';
function typeWriter(container) {
  if (i < txt.length) {
    document.getElementById(container).innerHTML += txt.charAt(i);
    i++;
    setTimeout(typeWriter, speed, container);
  }
}

